def _pair_row(message: str, title: str, match: int, storage_gb: int | None = None, color: str | None = None) -> dict:
    row = {
        "message": message,
        "product_title": title,
        "match": match,
        "keywords": TITLE_KEYWORDS.get(title, [title.lower()]),
    }
    if storage_gb is not None:
        row["storage_gb"] = storage_gb
    if color is not None:
        row["color"] = color
    if message in VARIANT_PAIR_MESSAGES or storage_gb is not None or color is not None:
        row["eval_scope"] = "variant"
    else:
        row["eval_scope"] = "skip"
    return row


def write_jsonl(path: Path, rows: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")


def main():
    train_rows = []
    datasets = [
        ("buy", BUY + VARIANT_BUY),
        ("sell", SELL + VARIANT_SELL),
        ("discussion", DISCUSSION + VARIANT_DISCUSSION),
        ("none", NONE + VARIANT_NONE_COLOR),
    ]
    for label, texts in datasets:
        for text in texts:
            train_rows.append({"text": text, "label": label})

    eval_rows = []
    eval_buy = BUY[:12] + VARIANT_BUY[:8]
    eval_sell = SELL[:12] + VARIANT_SELL[:8]
    eval_discussion = DISCUSSION[:12] + VARIANT_DISCUSSION[:8]
    eval_none = NONE[:12] + VARIANT_NONE_COLOR[:8]
    for text in eval_buy:
        eval_rows.append({"text": text, "label": "buy"})
    for text in eval_sell:
        eval_rows.append({"text": text, "label": "sell"})
    for text in eval_discussion:
        eval_rows.append({"text": text, "label": "discussion"})
    for text in eval_none:
        eval_rows.append({"text": text, "label": "none"})

    pair_rows = [_pair_row(*row) for row in PRODUCT_PAIRS]

    write_jsonl(DATA / "intent_train.jsonl", train_rows)
    write_jsonl(DATA / "intent_eval.jsonl", eval_rows)
    write_jsonl(DATA / "product_pairs_eval.jsonl", pair_rows)
    print(f"train={len(train_rows)} eval={len(eval_rows)} pairs={len(pair_rows)}")


if __name__ == "__main__":
    main()
